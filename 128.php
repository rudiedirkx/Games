}

function get_rand_num( lower, upper )
{
	if ( upper < lower )
	{
		tmp = lower;
		lower = upper;
		upper = tmp;
	}

	diff = upper - lower;
	rand = Math.random()
	rand_num = Math.round(lower + rand * diff);

	return rand_num;

}
